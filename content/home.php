                <p>
                    <img src="assets/images/banana.jpg" alt="img" height="200" class="pull-left img-rounded m-r-10">
                    Pisang adalah nama umum yang diberikan pada tumbuhan terna raksasa berdaun besar memanjang dari suku Musaceae. Beberapa jenisnya (Musa acuminata, M. balbisiana, dan M. ×paradisiaca) menghasilkan buah konsumsi yang dinamakan sama. Buah ini tersusun dalam tandan dengan kelompok-kelompok tersusun menjari yang disebut sisir. Hampir semua buah pisang memiliki kulit berwarna kuning ketika matang, meskipun ada beberapa yang berwarna jingga, merah, hijau, ungu, atau bahkan hampir hitam. Buah pisang sebagai bahan pangan merupakan sumber energi (karbohidrat) dan mineral, terutama kalium.</p>
                    
                    <p>Berdasarkan cara konsumsi buahnya, pisang dikelompokkan dalam dua golongan, yaitu pisang meja (dessert banana) dan pisang olah (plantain, cooking banana). Pisang meja dikonsumsi dalam bentuk segar setelah buah matang, seperti pisang ambon, susu, raja, seribu, dan sunripe. Pisang olahan dikonsumsi setelah digoreng, direbus, dibakar, atau dikolak, seperti pisang kepok, siam, kapas, tanduk, dan uli.</p>

                    <p>Selain memberikan kontribusi gizi lebih tinggi daripada apel, pisang juga dapat menyediakan cadangan energi dengan cepat bila dibutuhkan. Termasuk ketika otak mengalami keletihan. Beragam jenis makanan ringan dari pisang yang relatif populer antara lain Kripik Pisang asal Lampung, Sale pisang(Bandung), Pisang Molen (Bogor), dan epe (Makassar).</p>

                    <p>Pisang mempunyai kandungan gizi sangat baik, antara lain menyediakan energi cukup tinggi dibandingkan dengan buah-buahan lain. Pisang kaya mineral seperti kalium, magnesium, fosfor, besi, dan kalsium. Pisang juga mengandung vitamin, yaitu C, B kompleks, B6, dan serotonin yang aktif sebagai neurotransmitter dalam kelancaran fungsi otak.</p>
