                <p>
                    <img src="assets/images/mangoes.jpg" alt="img" height="200" class="pull-left img-rounded m-r-10">
                    Mangga atau mempelam adalah nama sejenis buah, demikian pula nama pohonnya. Mangga termasuk ke dalam marga Mangifera, yang terdiri dari 35-40 anggota dan suku Anacardiaceae. Nama ilmiahnya adalah Mangifera indica. Nama ini kira-kira mengandung arti: “(pohon) yang berbuah mangga, berasal dari India”.</p>

                    <p>Pohon mangga termasuk tumbuhan tingkat tinggi yang struktur batangnya (habitus) termasuk kelompok arboreus, yaitu tumbuhan berkayu yang mempunyai tinggi batang lebih dari 5 m. Mangga bisa mencapai tinggi antara 10 hingga 40 m.</p>

                    <p>Buah mangga termasuk kelompok buah batu (drupa) yang berdaging, dengan ukuran dan bentuk yang sangat berubah-ubah bergantung pada macamnya, mulai dari bulat (misalnya mangga gedong), bulat telur (gadung, indramayu, arumanis) hingga lonjong memanjang (mangga golek). Panjang buah kira-kira 2,5–30 cm. Pada bagian ujung buah, ada bagian yang runcing yang disebut paruh. Di atas paruh ada bagian yang membengkok yang disebut sinus, yang dilanjutkan ke bagian perut.</p>
                    
                    <p>Mangga terutama ditanam untuk buahnya. Buah yang matang umum dimakan dalam keadaan segar, sebagai buah meja atau campuran es, dalam bentuk irisan atau diblender. Buah yang muda kerapkali dirujak, atau dijajakan di tepi jalan setelah dikupas, dibelah-belah dan dilengkapi bumbu garam dengan cabai. Buah mangga juga diolah sebagai manisan, irisan buah kering, dikalengkan dan lain-lain. Di berbagai daerah di Indonesia, mangga (tua atau muda) yang masam kerap dijadikan campuran sambal atau masakan ikan dan daging.</p>
